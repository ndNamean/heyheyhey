/**
 * Bundle unified export API into a single Vercel serverless entrypoint.
 * Local relative imports are inlined; node_modules stay external.
 */

import * as esbuild from 'esbuild';
import { mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outfile = join(root, 'api', 'export.js');

mkdirSync(dirname(outfile), { recursive: true });

await esbuild.build({
  entryPoints: [join(root, 'api', '_lib', 'export', 'routes.js')],
  bundle: true,
  platform: 'node',
  format: 'esm',
  outfile,
  packages: 'external',
  logLevel: 'info',
  banner: {
    js: '/* Generated by scripts/bundle-export-api.mjs — do not edit by hand */',
  },
});

console.log('Bundled export API → api/export.js');
